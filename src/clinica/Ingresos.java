        //LISTAR PACIENTES
        public void ListarPaciente(){
            paciente=db.getListadosPaciente();
            DefaultTableModel tb=(DefaultTableModel)jTablePACIENTES.getModel();
            //limpio la tabla
            for (int i = 0; i < jTablePACIENTES.getRowCount(); i++) {
                tb.removeRow(i);
                i-=1;
            }
            for(Paciente pa:paciente ){
                tb.addRow(new Object[]{pa.getDni(),pa.getNombres(),pa.getApellidos(),pa.getDireccion(),pa.getTelefono(),pa.getSeguro_social()});
            }
        }
        
        //BUSCAR PACIENTES
        public void BuscarPaciente(){
           
            pacientebuscar=db.getSelect(jTextField5.getText());
            DefaultTableModel tb=(DefaultTableModel)jTablePACIENTES.getModel();
            //limpio la tabla
            for (int i = 0; i < jTablePACIENTES.getRowCount(); i++) {
                tb.removeRow(i);
                i-=1;
            }
            //recien lo lleno
            Paciente pa=pacientebuscar;
            tb.addRow(new Object[]{pa.getDni(),pa.getNombres(),pa.getApellidos(),pa.getDireccion(),pa.getTelefono(),pa.getSeguro_social()});
   
        }
        
        //LISTAR Habitaciones disponibles
        public void ListarHabitacion(){
            habitacion=dbha.getListadosHabitacion();
            DefaultTableModel tbha=(DefaultTableModel)jTableHabitacion.getModel();
            for(Habitacion ha:habitacion ){
                tbha.addRow(new Object[]{ha.getNombre(),ha.getCodigo(), ha.getNumero(),ha.getLetra()});
            }
        }
        
